package edu.sbu.control;

public class Customer {
	private String firstname;
	private String lastname;
	private String policyno;
	private String phoneno;
	private String email;
	private String policyco;
	private String AAA;
	private String username;
	
	public void setFirstname(String input){
		this.firstname = input;
	}
	public String getFirstname(){
		return this.firstname;
	}
	
	public void setLastname(String input){
		this.lastname = input;
	}
	public String getLastname(){
		return this.lastname;
	}
	
	public void setPolicyno(String input){
		this.policyno = input;
	}
	public String getPolicyno(){
		return this.policyno;
	}
	
	public void setPhoneno(String input){
		this.phoneno = input;
	}
	public String getPhoneno(){
		return this.phoneno;
	}
	
	public void setEmail(String input){
		this.email = input;
	}
	public String getEmail(){
		return this.email;
	}
	
	public void setPolicyco(String input){
		this.policyco = input;
	}
	public String getPolicyco(){
		return this.policyco;
	}
	
	public void setAAA(String input){
		this.AAA = input;
	}
	public String getAAA(){
		return this.AAA;
	}
	
	public void setUsername(String input){
		this.username = input;
	}
	public String getUsername(){
		return this.username;
	}
}
